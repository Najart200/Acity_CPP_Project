    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed

        try {
            String name = jTextField2.getText();
            String gender = jCheckBox1.isSelected() ? "Male" : jCheckBox2.isSelected() ? "Female" : "";
            String contact = jTextField4.getText();
            String roomNumber = (String) jComboBox1.getSelectedItem();
            String roomType = (String) jComboBox2.getSelectedItem();
            String checkIn = jFormattedTextField2.getText();
            String checkOut = jFormattedTextField3.getText();

            // Validate inputs
            if (name.isEmpty() || gender.isEmpty() || contact.isEmpty() || roomNumber.equals("__") || roomType.isEmpty() || checkIn.isEmpty() || checkOut.isEmpty()) {
                JOptionPane.showMessageDialog(this, "All fields are required!", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            // Parse dates
            LocalDate checkInDate = LocalDate.parse(checkIn, DateTimeFormatter.ofPattern("dd/MM/yyyy"));
            LocalDate checkOutDate = LocalDate.parse(checkOut, DateTimeFormatter.ofPattern("dd/MM/yyyy"));

            if (checkOutDate.isBefore(checkInDate)) {
                JOptionPane.showMessageDialog(this, "Check-out date must be after check-in date!", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            // Check for room availability
            for (int i = 0; i < tableModel.getRowCount(); i++) {
                String existingRoom = (String) tableModel.getValueAt(i, 3);
                if (roomNumber.equals(existingRoom)) {
                    JOptionPane.showMessageDialog(this, "Room " + roomNumber + " is already booked!", "Error", JOptionPane.ERROR_MESSAGE);
                    return;
                }
            }

            // Calculate total cost
            calculateTotalCost();
            String totalCostStr = jFormattedTextField4.getText();
            if (totalCostStr.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Total cost calculation failed! Ensure all inputs are valid.", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            double totalCost = Double.parseDouble(totalCostStr.replace("GH₵", "").replace(",", ""));

            // Create Customer and Room objects
            Customer customer = new Customer(0, name, gender, contact); // customerId will be set by DAO
            Room room = new Room(0, 0, roomNumber, roomType, checkInDate, checkOutDate, totalCost); // roomId and customerId will be set by DAO

            // Save to database
            reservationDAO.addReservation(customer, room);

            // Refresh table
            loadReservations();
            
            // Show success pop-up
            JOptionPane.showMessageDialog(this, "✓ Reservation Successfully saved to database", "Success", JOptionPane.INFORMATION_MESSAGE);
            clearFields();
        } catch (java.time.format.DateTimeParseException e) {
            JOptionPane.showMessageDialog(this, "Invalid input! Dates must be in DD/MM/YYYY format (e.g., 24/05/2025).", "Error", JOptionPane.ERROR_MESSAGE);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Error parsing total cost: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        } catch (SQLException ex) {
            Logger.getLogger(ReservationGUI.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_btnAddActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed

        int selectedRow = tblReservation.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Please select a reservation to delete.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        int confirm = JOptionPane.showConfirmDialog(this, "Are you sure you want to delete this reservation?", "Confirm Delete", JOptionPane.YES_NO_OPTION);
        if (confirm == JOptionPane.YES_OPTION) {
            try {
                String roomNumber = (String) tableModel.getValueAt(selectedRow, 3);
                reservationDAO.deleteReservation(roomNumber);
                loadReservations();
                
                clearFields();
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(this, "Error deleting reservation: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            }
        }

    }//GEN-LAST:event_btnDeleteActionPerformed

