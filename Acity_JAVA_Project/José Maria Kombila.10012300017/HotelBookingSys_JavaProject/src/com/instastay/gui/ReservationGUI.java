    private void calculateTotalCost() {
        try {
            String checkIn = jFormattedTextField2.getText();
            String checkOut = jFormattedTextField3.getText();
            String roomType = (String) jComboBox2.getSelectedItem();

            if (checkIn.isEmpty() || checkOut.isEmpty() || roomType.isEmpty()) {
                jFormattedTextField4.setText("");
                return;
            }

            LocalDate checkInDate = LocalDate.parse(checkIn, DateTimeFormatter.ofPattern("dd/MM/yyyy"));
            LocalDate checkOutDate = LocalDate.parse(checkOut, DateTimeFormatter.ofPattern("dd/MM/yyyy"));

            if (checkOutDate.isBefore(checkInDate)) {
                jFormattedTextField4.setText("");
                return;
            }

            long days = ChronoUnit.DAYS.between(checkInDate, checkOutDate);
            double SINGLE_ROOM_RATE = 400; // GH₵400 per night
            double DOUBLE_ROOM_RATE = 450; // GH₵450 per night
            double rate = roomType.equals("Single") ? SINGLE_ROOM_RATE : DOUBLE_ROOM_RATE;
            double totalCost = days * rate;

            jFormattedTextField4.setValue(totalCost);
        } catch (Exception e) {
            jFormattedTextField4.setText("");
            System.out.println("Error calculating total cost: " + e.getMessage());
        }
    }

