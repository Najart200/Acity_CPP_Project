    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:
        if (selectedId == -1) {
        JOptionPane.showMessageDialog(this, "Select a student first!");
        return;
    }

    try {
        String name = txtName.getText();
        int age = Integer.parseInt(cmbAge.getSelectedItem().toString());
        String gender = chkMale.isSelected() ? "Male" : "Female";
        String phone = txtPhone.getText();

        Student s = new Student(selectedId, name, age, gender, phone);
        dao.updateStudent(s);
        dao.loadTable(model);
        JOptionPane.showMessageDialog(this, "Student updated!");

    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Failed to update student");
    }
    }//GEN-LAST:event_btnEditActionPerformed
    
