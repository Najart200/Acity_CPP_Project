    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        if (selectedId == -1) {
        JOptionPane.showMessageDialog(this, "Select a student first!");
        return;
    }

    try {
        dao.deleteStudent(selectedId);
        dao.loadTable(model);
        JOptionPane.showMessageDialog(this, "Student deleted!");
        selectedId = -1;
    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Failed to delete student");
    }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void txtSearchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtSearchKeyReleased
        // TODO add your handling code here:
        String search = txtSearch.getText().toLowerCase();
    for (int i = 0; i < tblStudents.getRowCount(); i++) {
        String fullName = tblStudents.getValueAt(i, 1).toString().toLowerCase();
        if (fullName.contains(search)) {
            tblStudents.setRowSelectionInterval(i, i);
            break;
        }
    }
    }//GEN-LAST:event_txtSearchKeyReleased

    private void tblStudentsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblStudentsMouseClicked
        // TODO add your handling code here:
        int row = tblStudents.getSelectedRow();
    selectedId = Integer.parseInt(tblStudents.getValueAt(row, 0).toString());
    txtName.setText(tblStudents.getValueAt(row, 1).toString());
    cmbAge.setSelectedItem(tblStudents.getValueAt(row, 2).toString());

    String gender = tblStudents.getValueAt(row, 3).toString();
    chkMale.setSelected(gender.equals("Male"));
    chkFemale.setSelected(gender.equals("Female"));

    txtPhone.setText(tblStudents.getValueAt(row, 4).toString());
    }//GEN-LAST:event_tblStudentsMouseClicked

    private void btnRefreshActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRefreshActionPerformed
        // TODO add your handling code here:
        dao.loadTable(model); // Reloads the table from the database
    selectedId = -1; // Reset selection
    tblStudents.clearSelection();
    txtName.setText("");
    cmbAge.setSelectedIndex(0); // or clear as needed
    chkMale.setSelected(false);
    chkFemale.setSelected(false);
    txtPhone.setText("");
    }//GEN-LAST:event_btnRefreshActionPerformed

