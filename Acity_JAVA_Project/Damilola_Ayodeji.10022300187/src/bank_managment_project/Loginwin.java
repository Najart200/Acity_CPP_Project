    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
         String enteredUsername = tfUsername.getText(); 
    String enteredPassword = new String(tfPassword.getPassword()); 

    if (enteredUsername.isEmpty() || enteredPassword.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please enter both username and password.");
        return;
    }

    try (Connection conn = DBConnect.getConnection()) {
        String sql = "SELECT * FROM useraccounts WHERE username = ? AND password = ?";
        PreparedStatement pst = conn.prepareStatement(sql);
        pst.setString(1, enteredUsername);
        pst.setString(2, enteredPassword);  // 🔒 Later, compare hashed passwords

        ResultSet rs = pst.executeQuery();

        if (rs.next()) {
            // Login successful
            String welcomeMsg = "<html><h2 style='color:blue;'>🎉 Welcome, <b>" + enteredUsername + "</b>!</h2>"
                      + "<p>Glad to have you back. Redirecting to your dashboard...</p></html>";

            JOptionPane.showMessageDialog(this, welcomeMsg, "Login Successful 🎊", JOptionPane.INFORMATION_MESSAGE);

            Dashboard main = new Dashboard();
            main.setVisible(true);
            this.dispose(); 
        } else {
            // Invalid credentials
            String errorMsg = "<html><h2 style='color:red;'>❌ Login Failed</h2>"
                    + "<p>Invalid username or password.<br>Please try again.</p></html>";

            JOptionPane.showMessageDialog(this, errorMsg, "Authentication Error", JOptionPane.ERROR_MESSAGE);
        }

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error connecting to database: " + e.getMessage());
    }
    }//GEN-LAST:event_btnLoginActionPerformed

    private void btnCreateAccountActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateAccountActionPerformed
          String username = tfUsername.getText();
    String password = new String(tfPassword.getPassword());

    if (username.isEmpty() || password.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please enter both username and password.");
        return;
    }

    try (Connection conn = DBConnect.getConnection()) {

        String sql = "INSERT INTO useraccounts (username, password) VALUES (?, ?)";
        PreparedStatement pst = conn.prepareStatement(sql);
        pst.setString(1, username);
        pst.setString(2, password);  // 🔒 You can hash this later

        pst.executeUpdate();
        JOptionPane.showMessageDialog(this,
                "<html><h2 style='color:green;'>✅ Account created successfully!</h2><p>Welcome, " + username + "</p></html>",
                "Success", JOptionPane.INFORMATION_MESSAGE);

    } catch (SQLException e) {
        if (e.getMessage().contains("Duplicate")) {
            JOptionPane.showMessageDialog(this,
                    "<html><h3 style='color:red;'>❗ Username already exists.</h3><p>Please try a different one.</p></html>",
                    "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(this,
                    "<html><h3 style='color:red;'>Error creating account.</h3><p>" + e.getMessage() + "</p></html>",
                    "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
    }//GEN-LAST:event_btnCreateAccountActionPerformed

