    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
     String accNo = tfAccNum.getText();
    String accHolder = tfAccHolder.getText();
    String balanceStr = tfEnterBal.getText();

    if (accNo.isEmpty() || accHolder.isEmpty() || balanceStr.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please fill all fields.", "Missing Info", JOptionPane.WARNING_MESSAGE);
        return;
    }

    double balance;
    try {
        balance = Double.parseDouble(balanceStr);
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Invalid balance amount.", "Input Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    try (Connection con = DBConnect.getConnection()) {
        String sql = "INSERT INTO accounts (account_no,account_type, account_name, balance) VALUES (?, ?, ?, ?)";
        PreparedStatement ps = con.prepareStatement(sql);
        ps.setInt(1, Integer.parseInt(accNo));
        ps.setString(2, "checking");
        ps.setString(3, accHolder);
        ps.setDouble(4, balance); // Save numeric value to DB
        

        int rows = ps.executeUpdate();

        if (rows > 0) {
            JOptionPane.showMessageDialog(this, "Checking Account successfully saved to the database!", "Success", JOptionPane.INFORMATION_MESSAGE);

            if (dashboardRef != null) {
                // Add the new account directly to the table with $ formatted balance
                DefaultTableModel model = dashboardRef.getAccountTableModel();
                String formattedBalance = String.format("$%.2f", balance);
                model.addRow(new Object[] {
                    accNo,
                    "checking",
                    accHolder,
                    formattedBalance
                });

                // Update view and close this window
                dashboardRef.updateTable();
                dispose();
                dashboardRef.setVisible(true);
            }
        }

    } catch (SQLException e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Error saving account to database.", "DB Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnBackActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBackActionPerformed
     if (dashboardRef != null) {
        // Refresh the Dashboard's table from the DB
        dashboardRef.updateTable();
        dashboardRef.setVisible(true);
    } else {
        JOptionPane.showMessageDialog(this, "Dashboard is not available!", "Error", JOptionPane.ERROR_MESSAGE);
    }

    // Close this window (e.g., Create_Checking or Transfer_Money)
    dispose();
    }//GEN-LAST:event_btnBackActionPerformed

