    public void loadAccountsIntoTable() {
    DefaultTableModel model = getAccountTableModel();
    model.setRowCount(0); // Clear existing rows

    Connection con = null;
    PreparedStatement ps = null;
    ResultSet rs = null;

    try {
        con = DBConnect.getConnection();
        if (con == null) {
            System.out.println("Connection failed");
            return;
        }

        String sql = "SELECT account_no,account_type, account_name, balance FROM accounts";
        ps = con.prepareStatement(sql);
        rs = ps.executeQuery();

        while (rs.next()) {
            int accNo = rs.getInt("account_no");
            String accType = rs.getString("account_type");
            String name = rs.getString("account_name");
            double balance = rs.getDouble("balance");
            
            String formattedBalance = String.format("$%.2f", balance);

            // Add a row to the JTable model
            model.addRow(new Object[]{accNo, accType, name, formattedBalance});

        }

    } catch (SQLException e) {
        e.printStackTrace();
    } finally {
        try {
            if (rs != null) rs.close();
            if (ps != null) ps.close();
            if (con != null) con.close();
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }
}
    

