    private void btnDeleteAccActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteAccActionPerformed

          int selectedRow = TableAvailableAcc.getSelectedRow();

    if (selectedRow >= 0) {
        int confirm = JOptionPane.showConfirmDialog(
            null,
            "Are you sure you want to delete this account?",
            "Confirm Delete",
            JOptionPane.YES_NO_OPTION
        );

        if (confirm == JOptionPane.YES_OPTION) {
            // Get account number from table
            int accNo = (int) TableAvailableAcc.getValueAt(selectedRow, 0);

            Connection con = null;
            PreparedStatement ps = null;

            try {
                con = DBConnect.getConnection();
                String sql = "DELETE FROM accounts WHERE account_no = ?";
                ps = con.prepareStatement(sql);
                ps.setInt(1, accNo);

                int rowsAffected = ps.executeUpdate();

                if (rowsAffected > 0) {
                    // Remove from table model
                    DefaultTableModel model = (DefaultTableModel) TableAvailableAcc.getModel();
                    model.removeRow(selectedRow);

                    JOptionPane.showMessageDialog(null, "Account deleted successfully!");
                } else {
                    JOptionPane.showMessageDialog(null, "Failed to delete the account.");
                }
            } catch (SQLException ex) {
                ex.printStackTrace();
                JOptionPane.showMessageDialog(null, "Error: " + ex.getMessage());
            } finally {
                try {
                    if (ps != null) ps.close();
                    if (con != null) con.close();
                } catch (SQLException ex) {
                    ex.printStackTrace();
                }
            }
        }
    } else {
        JOptionPane.showMessageDialog(null, "Please select a row to delete.");
    }
    }//GEN-LAST:event_btnDeleteAccActionPerformed

