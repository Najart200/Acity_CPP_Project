    private void btnCalculate1btnCalculateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalculate1btnCalculateActionPerformed

        if (txtName.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please enter customer's name", "Missing Customer's name", JOptionPane.WARNING_MESSAGE);
            txtName.requestFocus();
            return;
        } else if (txtAddress.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please enter customer's address", "Missing Customer's address", JOptionPane.WARNING_MESSAGE);
            txtAddress.requestFocus();
            return;
        } else if (txtAmountLoan.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please enter the Loan Amount", "Missing Loan Amount", JOptionPane.WARNING_MESSAGE);
            txtAmountLoan.requestFocus();
            return;
        } else if (txtInterestRate.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please enter the Interest Rate", "Missing Loan Amount", JOptionPane.WARNING_MESSAGE);
            txtInterestRate.requestFocus();
            return;
        }

        interestRate = Double.parseDouble(txtInterestRate.getText()) / 100 / 12;
        numberofYears = Double.parseDouble(txtNumberOfYears.getText()) * 12;
        loanAmount = Double.parseDouble(txtAmountLoan.getText());

        monthlyPayment = Pmt(interestRate, numberofYears, loanAmount);
        totalPayment = monthlyPayment * numberofYears;

        lblMonthlyPayment.setText(String.format("$%,.2f", monthlyPayment));
        lblTotalPayment.setText(String.format("$%,.2f", totalPayment));
    }//GEN-LAST:event_btnCalculate1btnCalculateActionPerformed

    private void btnExit1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExit1ActionPerformed
        int iExit = JOptionPane.showConfirmDialog(null, "Are you sure you want to exit?","Loan Calculator",
            JOptionPane.YES_NO_OPTION,JOptionPane.QUESTION_MESSAGE);

        if (iExit == JOptionPane.YES_OPTION){
            DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
            Vector<Vector> tableData = model.getDataVector();

            try {
                try (FileOutputStream file = new FileOutputStream("file.bin");
                    ObjectOutputStream output = new ObjectOutputStream(file)) {

                    // Method for serialization of object
                    output.writeObject(tableData);

                }
            }
            catch (IOException ex) {
            }
            System.exit(0);
        }

    }//GEN-LAST:event_btnExit1ActionPerformed

